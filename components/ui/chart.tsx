// Helper to extract item config from a payload.
function getPayloadConfigFromPayload(
  config: ChartConfig,
  payload: unknown,
  key: string
) {
  if (typeof payload !== "object" || payload === null) {
    return undefined;
  }

  const payloadPayload =
    "payload" in payload &&
    typeof payload.payload === "object" &&
    payload.payload !== null
      ? payload.payload
      : undefined;

  let configLabelKey: string = key;

  if (
    key in payload &&
    typeof payload[key as keyof typeof payload] === "string"
  ) {
    configLabelKey = payload[key as keyof typeof payload] as string;
  } else if (
    payloadPayload &&
    key in payloadPayload &&
    typeof payloadPayload[key as keyof typeof payloadPayload] === "string"
  ) {
    configLabelKey = payloadPayload[
      key as keyof typeof payloadPayload
    ] as string;
  }

  return configLabelKey in config
    ? config[configLabelKey]
    : config[key as keyof typeof config];
}

import {
  Chart as ChartJS,
  CategoryScale,
  LinearScale,
  PointElement,
  LineElement,
  BarElement,
  ArcElement,
  Title,
  Tooltip,
  Legend,
  type ChartData,
  type ChartOptions,
} from "chart.js";
import { Line, Bar, Doughnut, Pie } from "react-chartjs-2";

// Register ChartJS components
ChartJS.register(
  CategoryScale,
  LinearScale,
  PointElement,
  LineElement,
  BarElement,
  ArcElement,
  Title,
  Tooltip,
  Legend
);

// Define types for our chart components
export interface LineChartProps {
  data: ChartData<"line">;
  options?: ChartOptions<"line">;
}

export interface BarChartProps {
  data: ChartData<"bar">;
  options?: ChartOptions<"bar">;
}

export interface DoughnutChartProps {
  data: ChartData<"doughnut">;
  options?: ChartOptions<"doughnut">;
}

export interface PieChartProps {
  data: ChartData<"pie">;
  options?: ChartOptions<"pie">;
}

// Export chart components
export function LineChart({ data, options }: LineChartProps) {
  return <Line data={data} options={options} />;
}

export function BarChart({ data, options }: BarChartProps) {
  return <Bar data={data} options={options} />;
}

export function DoughnutChart({ data, options }: DoughnutChartProps) {
  return <Doughnut data={data} options={options} />;
}

export function PieChart({ data, options }: PieChartProps) {
  return <Pie data={data} options={options} />;
}

export { ChartContainer, ChartStyle };
